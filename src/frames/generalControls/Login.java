    private void btnLogginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogginActionPerformed
        if ("".equals(txt_user.getText())) {
            JOptionPane.showMessageDialog(null, "Falta escribir el nombre del usuario", "¡Atención!", JOptionPane.WARNING_MESSAGE);
        } else if ("".equals(txt_pasword.getText())) {
            JOptionPane.showMessageDialog(null, "Falta escribir la contraseña", "¡Atención!", JOptionPane.WARNING_MESSAGE);
        } else {
            String user = txt_user.getText().replace(',', ' ');
            String pwd = txt_pasword.getText().replace(',', ' ');
            inicio_sesion(user, pwd);
        }
    }//GEN-LAST:event_btnLogginActionPerformed

