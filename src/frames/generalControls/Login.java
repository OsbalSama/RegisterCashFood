    public void upTryalVersion() {
        if (!new Configurations().isActivated() && !new Sesiones().areLogginsAtDay(new SimpleDateFormat("dd-MM-YYYY").format(new Date()))) {
            new Configurations().upTryalDays();
        }
    }

    public void inicio_sesion(String usuario, String contraseña) {
        try {
            System.out.println("login");
            if (new Usuarios().confirmar_acceso(usuario, contraseña)) {
                String id = new Sesiones().genID();
                upTryalVersion();
                new Sesiones().saveRegSession(id, usuario);
                String[] User = new Usuarios().findUserbyName(usuario);
                System.out.println("user"+User);
                if (User[3].equals("Administrador") || User[3].equals("root")) {
                    adminHome m = new adminHome();
                    m.Inicialize(usuario, User[3]);
                    m.show();
                } else {
                    userHome m = new userHome();
                    m.Inicialize(usuario, User[3]);
                    m.show();
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "<html><h1>¡ACCESO DENEGADO!</h1><font SIZE=5><p>Verifique sus credenciales y vuelva a intentarlo...</font></html>", "", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

