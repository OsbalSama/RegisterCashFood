    public boolean abierto(File af) {
        boolean resp = true;
        try {
            File file = new File(af.getAbsolutePath());
            File sameFileName = new File(af.getAbsolutePath());
            if (file.renameTo(sameFileName)) {
                resp = true;
            } else {
                resp = false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
        return resp;
    }

    public boolean compCacillas() {
        boolean resp = false;
        if ("".equals(txt_file.getText())) {
            txt_file.requestFocus();
            resp = true;
        }
        return resp;
    }

    public void ejecutar() {
        try {
            if (!compCacillas()) {
                File af = new File(txt_file.getText());
                if (abierto(af)) {
                    if (af.exists()) {
                        if (new CSVData().isEmptyFile(af.getAbsolutePath())) {
                            this.dispose();
                            SelectCols sc = new SelectCols(null, true);
                            String[] colssel = new CSVData().getHeaderCSV(af.getAbsolutePath());
                            sc.actualizar_combos(colssel);
                            sc.show();
                            if (sc.getFselected() != null) {
                                int[] csel = sc.getFselected();
                                new CSVData().saveDataBase(txt_file.getText(), csel);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Este archivo esta vacio, favor de verificar la ruta y vuelve a intentarlo", "¡ERROR!", JOptionPane.WARNING_MESSAGE);
                            txt_file.setText("");
                            btnBrowseFile.requestFocus();
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Este archivo no existe, favor de verificar la ruta y vuelve a intentarlo", "¡ERROR!", JOptionPane.WARNING_MESSAGE);
                        txt_file.setText("");
                        txt_file.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El archivo esta abierto, favor de cerrar el documento antes de cargarlo en la base de datos.", "¡Atencion!", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

