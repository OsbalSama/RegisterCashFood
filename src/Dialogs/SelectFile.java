    private void btnBrowseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseFileActionPerformed
        try {
            JFileChooser frame = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivo CSV", "csv", "CSV");
            frame.setFileFilter(filtro);
            frame.setAcceptAllFileFilterUsed(false);
            int returnVal = frame.showOpenDialog(null);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                if (getExtension(frame.getSelectedFile().getAbsolutePath()).equals("csv")) {
                    txt_file.setText(frame.getSelectedFile().getAbsolutePath());
                } else {
                    JOptionPane.showMessageDialog(null, "Extencion no valida, serciorase que el archivo sea CSV...", "¡Atencion!", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBrowseFileActionPerformed

    public String getExtension(String ruta) {
        String resp = "";
        int i = ruta.lastIndexOf('.');
        if (i > 0) {
            resp = ruta.substring(i + 1);
        }
        return resp;
    }

