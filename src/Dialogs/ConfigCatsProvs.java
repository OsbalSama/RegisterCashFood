    public void newReg() {
        try {
            String nreg = JOptionPane.showInputDialog("ESCRIBA NUEVA CATEGORIA");
            if (nreg != null) {
                if (nreg.replace(" ", "").replace(",", "").length() > 0) {
                    if (!new CatProvClass().existCat(nreg.replace(",", " "))) {
                        new CatProvClass().insCat(nreg.replace(",", " "));
                    } else {
                        JOptionPane.showMessageDialog(null, "<html><h1>ESTA CATEGORIA YA EXISTE...</h1><font SIZE=5><p> Pruebe con un nombre diferente</font></html>", "ATENCION", JOptionPane.INFORMATION_MESSAGE);
                        newReg();
                    }
                }
            }
            loadCats();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e);
        }

    }

    public void delReg() {
        if (listItems.getSelectedValue() != null && !new CatProvClass().getDBCat().isEmpty()) {
            String reg = listItems.getSelectedValue().replace(",", " ");
            int dialogButton = JOptionPane.showConfirmDialog(null, "DESEA BORAR LA CATEGORIA " + reg + "?", "BORRAR CATEGORIA", JOptionPane.YES_NO_OPTION);
            if (dialogButton == 0) {
                new CatProvClass().delCat(reg.replace(",", " "));
            }
        }
        loadCats();
    }

    public void updateReg() {
        if (listItems.getSelectedValue() != null) {
            if (listItems.getSelectedValue() != null && !new CatProvClass().getDBCat().isEmpty()) {
                int dialogButton = JOptionPane.showConfirmDialog(null, "DESEA ACTUALIZAR EL REGISTRO " + listItems.getSelectedValue() + "?", "ACTUALIZAR REGISTRO", JOptionPane.YES_NO_OPTION);
                if (dialogButton == 0) {
                    String oreg = listItems.getSelectedValue().replace(",", " ");
                    String nreg = JOptionPane.showInputDialog("SE MODIFICARA LA CATEGORIA " + oreg + ", ESCRIBA LA NUEVA CATEGORIA");
                    if (nreg != null) {
                        if (nreg.replace(" ", "").replace(",", "").length() > 0) {
                            new CatProvClass().UpdateCat(oreg, nreg.replace(",", " "));
                        }
                    }
                    loadCats();
                }
            }
        }
    }

