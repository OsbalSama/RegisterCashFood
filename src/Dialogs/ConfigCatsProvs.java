    public void loadCats() {
        database = 1;
        lblTitle.setText("ADMINISTRAR CATEGORIAS");
        List Cats = new CatProvClass().getDBCat();
        DefaultListModel listModel = new DefaultListModel();
        if (!Cats.isEmpty()) {
            for (int i = 0; i < Cats.size(); i++) {
                listModel.addElement(Cats.get(i).toString());
            }
        } else {
            listModel.addElement("SIN CATEGORIAS");
        }
        listItems.setModel(listModel);
    }

