    public void loadPrinterSize() {
        String[] datos = new Configurations().getTicketConfigs();
        switch (datos[1]) {
            case "3":
                cmbSizes.setSelectedIndex(0);
                break;
            case "2":
                cmbSizes.setSelectedIndex(1);
                break;
            case "1":
                cmbSizes.setSelectedIndex(2);
                break;
        }
    }
    
