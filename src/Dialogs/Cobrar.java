    public void cobrar() {
        try {
            if (!"".equals(txtPayWith.getText()) && Double.parseDouble(txtPayWith.getText()) >= Double.parseDouble(new FormatoNumerico().RemoverFormatoMoneda(lblFinalPrice.getText()))) {
                asign_datos();
            } else {
                txtPayWith.setBackground(Color.yellow);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void asign_datos() {
        if (Double.parseDouble(txtPayWith.getText()) >= Double.parseDouble(new FormatoNumerico().RemoverFormatoMoneda(lblFinalPrice.getText()))) {
            result = Double.parseDouble(new FormatoNumerico().RemoverFormatoMoneda(txtPayWith.getText()));
            state = 1;
            setImpresora(combo_printers.getSelectedItem().toString());
            setTamanoHoja(cmbSizes.getSelectedIndex());
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "El monto ingresado es menor o igual al coste a pagar... por favor, corríjalo y continúe…", "¡Atención!", JOptionPane.INFORMATION_MESSAGE);
        }
    }

