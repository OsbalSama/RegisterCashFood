    private void txtPayWithKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPayWithKeyReleased
        if (!txtPayWith.getText().equals("")) {
            if (Double.parseDouble(txtPayWith.getText()) < Double.parseDouble(new FormatoNumerico().RemoverFormatoMoneda(lblFinalPrice.getText()))) {
                txtPayWith.setBackground(Color.RED);
                txtPayWith.setForeground(Color.WHITE);
            } else {
                txtPayWith.setBackground(Color.WHITE);
                txtPayWith.setForeground(Color.BLACK);
            }
            recalc_total();
        } else {
            txtCambio.setText(new FormatoNumerico().AsignarFormatoMoneda("0"));
            recalc_total();
        }
    }//GEN-LAST:event_txtPayWithKeyReleased

    public void recalc_total() {
        if (!txtPayWith.getText().equals("")) {
            double total = Double.parseDouble(txtPayWith.getText()) - Double.parseDouble(new FormatoNumerico().RemoverFormatoMoneda(lblFinalPrice.getText()));
            txtCambio.setText(String.valueOf(new FormatoNumerico().AsignarFormatoMoneda(total + "")));
        } else {
            txtCambio.setText(new FormatoNumerico().AsignarFormatoMoneda("0"));
        }
    }

