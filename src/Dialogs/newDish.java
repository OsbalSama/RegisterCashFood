    public void configCat() {
        lblConfigGroups.setForeground(Color.YELLOW);
        ConfigCatsProvs ncp = new ConfigCatsProvs(null, true);
        ncp.loadCats();
        ncp.show();
        loadCats();
        lblConfigGroups.setForeground(Color.BLUE);
    }

    public void loadCats() {
        List Cats = new CatProvClass().getDBCat();
        cmbCats.removeAllItems();
        if (!Cats.isEmpty()) {
            for (int i = 0; i < Cats.size(); i++) {
                cmbCats.addItem(Cats.get(i).toString());
            }
        } else {
            cmbCats.addItem("SIN CATEGORIAS");
        }
    }

    public void updateImage() {
        try {
            int resp = JOptionPane.showConfirmDialog(this, "¿Desea cambiar la foto del platillo?", "Continuar...", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                new Dishes().addDishPhoto(lblID.getText());
                setDishImage();
            }
            btnAceptar.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

    public boolean compCacillas() {
        boolean resp = false;
        if ("".equals(txt_desc.getText())) {
            txt_desc.requestFocus();
            resp = true;
        } else if ("".equals(txtPrecio.getText()) || Double.parseDouble(txtPrecio.getText()) == 0) {
            txtPrecio.requestFocus();
            resp = true;
        }
        return resp;
    }

    public void setDishImage() {
        try {
            lblImage.setIcon(new Dishes().getDishIcon(lblID.getText(), lblImage.getWidth() - 5, lblImage.getHeight() - 5));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void saveDish() {
        if (!compCacillas()) {
            String nreg = lblID.getText()
                    + "," + txtTitle.getText().replace(',', ' ')
                    + "," + txt_desc.getText().replace(',', ' ')
                    + "," + cmbCats.getSelectedItem().toString().replace(',', ' ')
                    + "," + new FormatoNumerico().RemoverFormatoMoneda(txtPrecio.getText())
                    + "," + new SimpleDateFormat("dd-MM-YYYY").format(new Date()) + ",";
            if (btnAceptar.getText().equals("GUARDAR")) {
                try {
                    new Dishes().InsDish(nreg);
                    JOptionPane.showMessageDialog(this, "NUEVO PLATILLO AÑADIDO CORRECTAMENTE", "INSERTAR PLATILLO", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e);
                }
            } else {
                try {
                    int dialogButton = JOptionPane.showConfirmDialog(null, "DESEA ACTUALIZAR EL PLATILLO SELECCIONADO?", "ACTUALIZAR PLATILLO", JOptionPane.YES_NO_OPTION);
                    if (dialogButton == 0) {
                        new Dishes().updateDish(nreg);
                        JOptionPane.showMessageDialog(this, "PLATILLO ACTUALIZADO CORRECTAMENTE", "ACTUALIZAR PLATILLO", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e);
                }
            }
        }
    }

    public void delProd() {
        int dialogButton = JOptionPane.showConfirmDialog(null, "DESEA BORRAR ESTE PLATILLO?", "BORRAR PLATILLO", JOptionPane.YES_NO_OPTION);
        if (dialogButton == 0) {
            if (new Dishes().delDish(lblID.getText())) {
                JOptionPane.showMessageDialog(null, "<html><h1>PLATILLO ELIMINADO CORRECTAMENTE</h1><font SIZE=5><p> Clic para cerrar</font></html>", "Terminado", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        }
    }

    public void InsertDish() {
        txtTitle.requestFocus();
        txtPrecio.setTransferHandler(null);
        txt_desc.setLineWrap(true);
        btnBorrar.setVisible(false);
        lblID.setText(new Dishes().genID());
        setDishImage();
        loadCats();
    }

