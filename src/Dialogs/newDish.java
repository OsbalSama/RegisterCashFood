    public void updateDish(String id) {
        jLabel1 .setText("VER PLATILLO");
        btnAceptar.setText("ACTUALIZAR");
        btnBorrar.setVisible(true);
        loadCats();
        String[] reg = new Dishes().getDishbyID(id).split(",");        
        lblID.setText(reg[0]);
        txtTitle.setText(reg[1]);
        txt_desc.setText(reg[2]);
        cmbCats.setSelectedItem(reg[3]);
        txtPrecio.setText(reg[4]);
        txtTitle.requestFocus();        
        setDishImage();
    }


