    public void printHistory() {
        try {
            if (modelo.getRowCount() > 0) {
                RePrint rp = new RePrint(null, true);
                rp.asign_datos();
                rp.show();
                if (rp.isResp()) {
                    String ticketData = lblIdClient.getText().replace(",", "") + ","
                            + getUser().replace(",", "") + ",";
                    String[] historial = getTableData();
                    new PDFDocs().ImprimirHistorialCliente(ticketData.split(","), historial, rp.getImpresora(), rp.getTamanoHoja());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error printHistory: " + e);
        }
    }

    public String[] getTableData() {
        String[] resp = new String[tableData.getRowCount()];
        for (int i = 0; i < resp.length; i++) {
            String temp = tableData.getValueAt(i, 0) + "," + tableData.getValueAt(i, 1) + "," + tableData.getValueAt(i, 2) + "," + tableData.getValueAt(i, 3) + "," + tableData.getValueAt(i, 4) + ",";
            resp[i] = temp;
        }
        return resp;
    }

