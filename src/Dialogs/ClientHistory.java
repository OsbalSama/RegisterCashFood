    public void inicialize(String idCLient, String user) {
        try {
            String[] cliente = new Clients().getClientbyID(idCLient);
            setUser(user);
            lblIdClient.setText(cliente[0]);
            txtaName.setText(cliente[1]);
            txtTel.setText(cliente[3]);
            loadTabla();
            refrershTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e);
        }
    }

    public void loadTabla() {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int filas, int columnas) {
                if (columnas < 0) {
                    return true;
                } else {
                    return false;
                }
            }
        };

        modelo.addColumn("Orden");
        modelo.addColumn("Mesa");
        modelo.addColumn("Total Coste");
        modelo.addColumn("Envio");
        modelo.addColumn("Estado");
        tableData.setModel(modelo);
    }

    public void refrershTable() {
        modelo.setRowCount(0);
        loadData();
    }

    public void loadData() {
        try {
            modelo.setRowCount(0);
            List<String> database = new Orders().getOrdersByClient(lblIdClient.getText());
            if (!database.isEmpty()) {
                for (int i = 0; i < database.size(); i++) {
                    modelo.addRow(database.get(i).split(","));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "Â¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

